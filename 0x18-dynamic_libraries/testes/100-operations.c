#include <stdio.h>

/**
 * mul - function to return multiplication of two numbers
 * @a: value 1
 * @b: value 2
 *
 * Return: the multiplication of two values
 */
int mul(int a, int b)
{
	return a * b;
}

/**
 * add - function to add two numbers
 * @a: value 1
 * @b: value 2
 *
 * Return: the addition  of two values
 */
int add(int a, int b)
{
        return a + b;
}

/**
 * sub - function to add two numbers
 * @a: value 1
 * @b: value 2
 *
 * Return: the subtraction of two values
 */
int sub(int a, int b)
{
        return a - b;
}

/**
 * div - function to divide two numbers
 * @a: value 1
 * @b: value 2
 *
 * Return: the division of two values
 */
int div(int a, int b)
{
        return a / b;
}

/**
 * div - function to find the modulus between two numbers
 * @a: value 1
 * @b: value 2
 *
 * Return: the modulus of two values
 */
int mod(int a, int b)
{
        return a % b;
}
